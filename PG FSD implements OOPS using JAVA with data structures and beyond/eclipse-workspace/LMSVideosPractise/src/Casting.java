
public class Casting {
	
	public static void main(String[] args) {
		double myDouble = 1.1;
		int myInt = (int) myDouble;
		System.out.println("The values are: " +(myDouble) +" and " + (myInt));
	}

	// upcasting - upper datatype to smaller datatype
	// downcasting - smaller datatype to upper datatype
	// reference doesnt contain the value it contains the object address
	// objects are stored in the heap 
}
